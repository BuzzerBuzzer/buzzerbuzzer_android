    private void fixgps(double x, double y, String title) {
        //Log.e("좌표3", x + "  : " + y);
        final String last_gps_x = String.valueOf(x);
        final String last_gps_y = String.valueOf(y);
        final String base_gps_address = title;

        // Log.e("좌표", last_gps_x + " : " + last_gps_y);
        StringRequest stringRequest = new StringRequest(Request.Method.POST, Config.REGISTER_URL,
                new Response.Listener<String>() {
                    @Override
                    public void onResponse(String response) {
                        Log.e(LOG_TAG,"fixgps "+response);
                        //Toast.makeText(getApplicationContext(), response, Toast.LENGTH_SHORT).show();
                        try {
                            JSONObject json = new JSONObject(response);

                            if (json.getString("result").equals("success")) {//성공시
                                //Toast.makeText(NMapViewer.this, "위치설정 완료", Toast.LENGTH_SHORT).show();
                                boolean setLocation = true;

                                Log.e(LOG_TAG+ "set_Location :", String.valueOf(setting.getBoolean("set_Location", false)));

                                ProfileSetting profileSetting = new ProfileSetting();

                                editor.putBoolean("set_Location", setLocation);
                                editor.putLong("set_location_time", profileSetting.firstTime());
                                editor.putString("set_base_gps_address_yj", base_gps_address);
                                editor.commit();

                                Log.e("set_location_time 확인 ", String.valueOf(setting.getLong("set_location_time", 0)));

                                Intent returnIntent = new Intent(NMapViewer.this, ProfileSetting.class);
                                returnIntent.putExtra("LOCATION", base_gps_address);

                                setResult(1000, returnIntent);
//                                startActivityForResult(returnIntent, 1000);
                                onBackPressed();
//                                finish();
                            } else {//json.getString("result").equals("fail")시
                                Toast.makeText(getApplicationContext(), "위치설정하기 실패하였습니다. 다시 시도해주세요.", Toast.LENGTH_SHORT).show();
                                return;
                            }
                        } catch (JSONException e) {
                            e.printStackTrace();
                        }

                    }
                },
                new Response.ErrorListener() {
                    @Override
                    public void onErrorResponse(VolleyError error) {//서버에러
                        Toast.makeText(getApplicationContext(), "인터넷을 연결을 확인해주세요.", Toast.LENGTH_LONG).show();
                    }
                }) {
            @Override
            protected Map<String, String> getParams() {
                Map<String, String> params = new HashMap<String, String>();
                params.put("param", jc.createJsonParam(id, mPassword, "android", Build.VERSION.RELEASE, Build.MODEL,"proc_12_update_base_gps", new String[]{id, last_gps_x, last_gps_y, base_gps_address, "normal", ""}));//서버 서비코드
                return params;
            }
        };
        RequestQueue requestQueue = Volley.newRequestQueue(this);
        requestQueue.add(stringRequest);
    }


